package encap.org;

public class Human {
	
	private int height;
	
	private int weight;
	
	private int bmi;

	public void setHeight(int height) {
		this.height = height;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public void setBmi(int bmi) {
		this.bmi = bmi;
		
	}

	
	
	
	
	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}

	public int getBmi() {
		return bmi;
	}
	
	
	
	
	

}
